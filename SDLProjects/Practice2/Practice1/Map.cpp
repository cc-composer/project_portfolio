Map::Map()
{
	dirt = TextureManager::LoadTexture("graphics/dirt.png");
	grass = TextureManager::LoadTexture("graphics/grass.png");
	water = TextureManager::LoadTexture("graphics/water.png");

	LoadMap(levelOne);
	source.x = source.y = 0;
	source.w = destination.w = 32;
	source.h = destination.h = 32;

	destination.x = destination.y = 0;
}

Map::~Map()
{
}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;
	
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			destination.x = column * 32;
			destination.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, source, destination);
				break;
			case 1:
				TextureManager::Draw(dirt, source, destination);
				break;
			case 2:
				TextureManager::Draw(grass, source, destination);
				break;
			default:
				SDL_Log("Invalid terrain being drawn.");
				break;
			}
		}
	}
}
